'''
dp[n] = K(개)
dp[1] = 1개  : 1
dp[2] = 2개  : 11    00
dp[3] = 3개  : 111   001   100
dp[4] = 5개  : 1111  0011  1001  1100  0000
dp[n]은 (n-1번째 값들 맨끝에 1 붙이기 + n-2 번째 값들 맨 끝에 00붙이기) 한 갯수
dp[n] = dp[n-1] + dp[n-2]
'''

N = int(input())

dp = [0] * (N+2)
dp[1] = 1
dp[2] = 2

for n in range(3, N+1):
    dp[n] = (dp[n-1]+dp[n-2]) % 15746

print(dp[N])
